	<td colspan="2" align="left" bordercolor="#070E12" class="row1"><p class="genmed">Divine Requiem was concieved in February of 2006, an mere idea in the mind of Tong Zou. After browsing and observing all the various anime and graphics sites out there, he figured that there many redundant and stereotypical attributes of everyone of them. He found sites like daydreamgraphics.com or celestial-star.net and found that they allowed the common community to become involved in the development. He found this idea really unique and took his inspiration from those sites. Divine Requiem is now a completely user-friendly site that offers a suitable gallery for uploading music, video, files, and documents. DR was originally going to be an anime/graphics community but the notion was abandoned shortly after Tong found out he could no longer keep up with the reviews and tutorials. <span class="genmed">
    </span>It is now a fully functioning site capable of indexing various types of media and files for other users. Divine Requiem also consists of a forum, the benefits of having a forum is that: </p>
	  <span class="genmed"><p>
  -A search engine is already built in.<br>
  -A user authentication system is already built in.<br>
  -Users can comment and submit information readily.<br>
  -The PHPBB forum can sort topics and provides downloads without the hassle of sql/php scripts.<br>
  -Forums can change layouts the same way a site can.<br>
  -Information is divided within the forum the same way a site can.<br>
  -HTML code can be used in the forum, meaning that it functions almost exactly like a regular site.<br>
  -The Dynamic user interaction system is inherently part of the forum.
      
	  <p>A new navigation system was added so that Divine Requiem and its forum could be linked easily.   
